    private void btnCadFuncActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadFuncActionPerformed
        //checar os campos
        if (!chkAdm.isSelected() && !chkFunc.isSelected()) {
            Component frame = null;
            JOptionPane.showMessageDialog(frame,
                    "Favor selecionar um perfil para o usuário!");
        } else if ("".equals(edtNomeFunc.getText())
                || "".equals(edtCrefito.getText())
                || "".equals(edtPhone.getText())
                || "".equals(edtEmailLogin.getText())
                || "".equals(edtPass1.getText())) {
            Component frame = null;
            JOptionPane.showMessageDialog(frame,
                    "Favor verificar se todos os campos foram preenchidos!");
        } else if (edtPass1.getText() == null ? edtPass2.getText() != null : !edtPass1.getText().equals(edtPass2.getText())) {
            Component frame = null;
            JOptionPane.showMessageDialog(frame,
                    "As senhas não coincidem!");
        } else if (!EmailValidator.getInstance().isValid(edtEmailLogin.getText())) {
            Component frame = null;
            JOptionPane.showMessageDialog(frame,
                    "O login não é um e-mail válido!");
        } else if (edtPass1.getText().length() < 6) {
            Component frame = null;
            JOptionPane.showMessageDialog(frame,
                    "O password deve ter ao menos 6 caracteres");
        } else if ("111111".equals(edtPass1.getText())) {
            Component frame = null;
            JOptionPane.showMessageDialog(frame,
                    "Senha inválida!");
        } else {

            //Google Agenda
            String calendarId = null;
            GoogleAgendaClientCalendar gaclical = new GoogleAgendaClientCalendar();

            // Create a new calendar
            com.google.api.services.calendar.model.Calendar calendar;
            calendar = new com.google.api.services.calendar.model.Calendar();
            calendar.setSummary(edtNomeFunc.getText());
            calendar.setTimeZone("America/Sao_Paulo");

            // Insert the new calendar
            com.google.api.services.calendar.model.Calendar createdCalendar;
            try {
                createdCalendar = gaclical.CalendarSVC().calendars().insert(calendar).execute();
                System.out.println(createdCalendar.getId());
                calendarId = createdCalendar.getId();
            } catch (GeneralSecurityException | IOException ex) {
                Logger.getLogger(Salas.class.getName()).log(Level.SEVERE, null, ex);
            }

            String perfil = null;
            //pegando o perfil do profissional
            if (chkAdm.isSelected() && chkFunc.isSelected()) {
                perfil = "AdminFunc";
            } else if (chkFunc.isSelected()) {
                perfil = "Func";
            } else if (chkAdm.isSelected()) {
                perfil = "Admin";
            }

            //registrando no auth 
            FireBaseAuth fba = new FireBaseAuth();
            String token = null;
            try {
                token = fba.cadastrar(edtEmailLogin.getText(), edtPass1.getText());
            } catch (Exception ex) {
                Component frame = null;
                JOptionPane.showMessageDialog(frame,
                        "Ocorreu o erro\n" + ex);
                Logger.getLogger(CadastroProfissional.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (token != null) {
                ModelCadastroFunc mcf = new ModelCadastroFunc();
                mcf.setNomeFunc(edtNomeFunc.getText());
                mcf.setCrefito(edtCrefito.getText());
                mcf.setPhone(edtPhone.getText());
                mcf.setProc1(comboProc1.getSelectedItem().toString());
                mcf.setProc2(comboProc2.getSelectedItem().toString());
                mcf.setProc3(comboProc3.getSelectedItem().toString());
                mcf.setCalendarId(calendarId);
                mcf.setPerfil(perfil);
                mcf.setLogin(edtEmailLogin.getText());
                mcf.setToken(token);
                mcf.setFlag(true);

                //registrando no banco de dados
                FireBaseClient fbcli = new FireBaseClient();

                DocumentReference docRef;
                docRef = fbcli.FirebaseInt().collection("funcionarios").document(token);
                ApiFuture<WriteResult> result = docRef.set(mcf);
                try {
                    System.out.println("Update time : " + result.get().getUpdateTime());
                    Component frame = null;
                    JOptionPane.showMessageDialog(frame,
                            "Profissional cadastrado com sucesso!");
                    edtNomeFunc.setText("");
                    edtCrefito.setText("");
                    edtPhone.setText("");
                    edtEmailLogin.setText("");
                    edtPass1.setText("");
                    edtPass2.setText("");
                    chkAdm.setSelected(false);
                    chkFunc.setSelected(false);
                    comboProc1.setSelectedItem("Selecione um Procedimento");
                    comboProc2.setSelectedItem("Selecione um Procedimento");
                    comboProc3.setSelectedItem("Selecione um Procedimento");

                } catch (InterruptedException | ExecutionException ex) {
                    Logger.getLogger(CadastroProfissional.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

        }

    }//GEN-LAST:event_btnCadFuncActionPerformed

