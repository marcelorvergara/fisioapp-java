    private void initComboProcs() throws InterruptedException, ExecutionException {

        FireBaseClient fbcli = new FireBaseClient();
        ApiFuture<QuerySnapshot> query = fbcli.FirebaseInt().collection("procedimentos").get();
        QuerySnapshot querySnapshot = query.get();
        List<QueryDocumentSnapshot> documents = querySnapshot.getDocuments();
        comboProc1.removeAllItems();
        comboProc2.removeAllItems();
        comboProc3.removeAllItems();

        comboProc1.addItem("Selecione um Procedimento");
        comboProc2.addItem("Selecione um Procedimento");
        comboProc3.addItem("Selecione um Procedimento");

        documents.forEach((QueryDocumentSnapshot document) -> {
            String procedimento = document.toObject(ModelProcedimento.class).getProcedimento();
            comboProc1.addItem(procedimento);
            comboProc2.addItem(procedimento);
            comboProc3.addItem(procedimento);
        });
    }

