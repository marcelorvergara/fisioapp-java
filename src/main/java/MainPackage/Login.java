    private void btnLoginMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnLoginMouseReleased
        String email = edtLogin.getText();
        String pass = edtPass.getText();
        FireBaseAuth fba = new FireBaseAuth();
        String token = null;
        String perfil = null;
        try {
            token = fba.auth(email, pass);

        } catch (Exception ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (token != null) {
            TelaPrincipal tp = null;
            try {
                FireBaseClient fbclient = new FireBaseClient();
                UserRecord userRecord = fbclient.FirebaseAuth().getUserByEmail(email);

                FireBaseClient fbcli = new FireBaseClient();
                DocumentReference docRef;
                docRef = fbcli.FirebaseInt().collection("funcionarios").document(userRecord.getUid());
                ApiFuture<DocumentSnapshot> future = docRef.get();
                DocumentSnapshot document = future.get();

                //verifica se é o primeiro acesso
                if (document.getBoolean("flag")) {
                    LoginTrocaSenha lts = new LoginTrocaSenha(document.getString("perfil"), edtLogin.getText(), userRecord.getUid());
                    lts.setVisible(true);

                    this.dispose();
                } else {
                    //acessa a tela principal de acordo com o perfil
                    perfil = document.getString("perfil");

                    tp = new TelaPrincipal(perfil);
                    tp.setVisible(true);
                    this.dispose();
                }

            } catch (InterruptedException | ExecutionException | FirebaseAuthException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            Component frame = null;
            JOptionPane.showMessageDialog(frame,
                    "Usuário ou senha inválidos");
        }
    }//GEN-LAST:event_btnLoginMouseReleased

    private void cadastriUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cadastriUsuarioMouseClicked
        if (edtLogin.getText().isEmpty()) {
            Component frame = null;
            JOptionPane.showMessageDialog(frame,
                    "Favor preencher o email no login");
        } else {
            FireBaseClient fbclient = new FireBaseClient();
            String link = null;
            try {
                link = fbclient.FirebaseAuth().generatePasswordResetLink(edtLogin.getText());
                sendCustomPasswordResetEmail(edtLogin.getText(), link);
            } catch (FirebaseAuthException ex) {
                String retorno = ex.toString().split("body:")[1];
                String erro = retorno.split("\"message\":")[1].split(",")[0];

                Component frame = null;
                JOptionPane.showMessageDialog(frame,
                        "Ocorreu o erro\n" + erro);
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_cadastriUsuarioMouseClicked

