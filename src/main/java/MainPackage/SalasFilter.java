    private void btnEdtSalaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEdtSalaActionPerformed
        if ("Selecione uma Sala".equals(comboSala.getSelectedItem().toString())) {
            Component frame = null;
            JOptionPane.showMessageDialog(frame,
                    "Favor verificar a Sala!");
        } else {
            String nomeSala = comboSala.getSelectedItem().toString();

            //buscar Sala
            ModelSala sala;
            sala = mdlSala.stream()
                    .filter((ModelSala it) -> nomeSala.equals(it.getNomeSala()))
                    .findAny()
                    .orElse(null);

            Salas sla;
            sla = new Salas(sala);
            sla.setVisible(true);
        }
    }//GEN-LAST:event_btnEdtSalaActionPerformed

