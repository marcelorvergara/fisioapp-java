    private void initComboSalas() throws InterruptedException, ExecutionException {
        //Combo das Salas
        FireBaseClient fbclipac = new FireBaseClient();

        ApiFuture<QuerySnapshot> querySala = fbclipac.FirebaseInt().collection("salas").get();

        QuerySnapshot querySnapshot = querySala.get();
        List<QueryDocumentSnapshot> documents;
        documents = querySnapshot.getDocuments();
        comboSala.removeAllItems();
        comboSala.addItem("Selecione uma Sala");
        documents.forEach((QueryDocumentSnapshot documentPac) -> {
            String nomePacote = documentPac.toObject(ModelSala.class
            ).getNomeSala();
            mdlSala.add(documentPac.toObject(ModelSala.class));
            comboSala.addItem(nomePacote);

        });

        ComboBoxSearchable searchablePac = new ComboBoxSearchable(comboSala);
    }
