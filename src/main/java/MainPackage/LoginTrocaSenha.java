    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        if (edtPass1.getText() == null ? edtPass2.getText() != null : !edtPass1.getText().equals(edtPass2.getText())) {
            Component frame = null;
            JOptionPane.showMessageDialog(frame,
                    "As senhas não coincidem!");
        } else if ("111111".equals(edtPass1.getText())) {
            Component frame = null;
            JOptionPane.showMessageDialog(frame,
                    "Senha inválida!");
        } else if (edtPass1.getText().length() < 6) {
            Component frame = null;
            JOptionPane.showMessageDialog(frame,
                    "O password deve ter ao menos 6 caracteres");
        } else {
            //trocar a senha
            FireBaseClient fbclient = new FireBaseClient();
            try {
                //troca no authentication                System.out.println("Write result: " + result);

                UserRecord userRecord = fbclient.FirebaseAuth().getUserByEmail(loginGlobal);
                UpdateRequest request = new UpdateRequest(userRecord.getUid())
                        .setPassword(edtPass1.getText());
                userRecord = FirebaseAuth.getInstance().updateUser(request);
                System.out.println("Successfully updated user: " + userRecord.getUid());

                //troca da flag de primeira senha
                DocumentReference docRef = fbclient.FirebaseInt().collection("funcionarios").document(uIdGlobal);
                ApiFuture<WriteResult> future = docRef.update("flag", false);
                WriteResult result = future.get();

            } catch (FirebaseAuthException | InterruptedException | ExecutionException ex) {
                Logger.getLogger(LoginTrocaSenha.class.getName()).log(Level.SEVERE, null, ex);
            }

            TelaPrincipal tp;
            try {
                tp = new TelaPrincipal(perfilGlobal);
                tp.setVisible(true);
                this.dispose();
            } catch (InterruptedException | ExecutionException ex) {
                Logger.getLogger(LoginTrocaSenha.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

