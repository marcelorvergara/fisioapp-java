    private void btnGerarRelatorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGerarRelatorioActionPerformed
        if ((dateIni.getDate() == null) || (dateFim.getDate() == null)) {
            Component frame = null;
            JOptionPane.showMessageDialog(frame,
                    "Verifique as datas");
        } else {
            java.util.Date dataIni = dateIni.getDate();
            Calendar calIni = Calendar.getInstance();
            calIni.setTime(dataIni);
            calIni.set(Calendar.MILLISECOND, 0);
            Timestamp tsIni = new Timestamp(calIni.getTimeInMillis());

            java.util.Date dataFim = dateFim.getDate();
            Calendar calFim = Calendar.getInstance();
            calFim.setTime(dataFim);
            calFim.set(Calendar.MILLISECOND, 0);
            Timestamp tsFim = new Timestamp(calFim.getTimeInMillis());

            String resp = comboFuncionarios.getSelectedItem().toString();

            ListaEntradas lstEntr = new ListaEntradas(tsIni, tsFim, resp);
            lstEntr.setVisible(true);
        }

    }//GEN-LAST:event_btnGerarRelatorioActionPerformed

