    private void initData(String value) throws ParseException {
        String dataAntiga = value.split(" - ")[0];

        String horaAntiga = value.split(" - ")[1];
        String horaIniAntiga = horaAntiga.split("/")[0];
        String horaFimAntiga = horaAntiga.split("/")[1];
        comboHoraInicio.setSelectedItem(horaIniAntiga.split(":")[0]);
        comboMinInicio.setSelectedItem(horaIniAntiga.split(":")[1]);
        comboHoraFim.setSelectedItem(horaFimAntiga.split(":")[0]);
        comboMinFim.setSelectedItem(horaFimAntiga.split(":")[1]);

        System.out.println("D A " + dataAntiga);//20-07-2020
        Date date1 = new SimpleDateFormat("dd-MM-yyyy").parse(dataAntiga);

        edtDia.setDate(date1);
    }
