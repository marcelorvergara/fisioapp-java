    private void btnInserirProcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirProcActionPerformed
        if ("".equals(edtNomeProc.getText())) {
            Component frame = null;
            JOptionPane.showMessageDialog(frame,
                    "O nome do procedimento não pode ser vazio");
        } else if ("Qtd. Máx. de Pacientes".equals(comboQtdPacientes.getSelectedItem().toString())) {
            Component frame = null;
            JOptionPane.showMessageDialog(frame,
                    "Favor escolher o número máximo de pacientes simultâneos para esse procedimento!");
        } else {
            ModelProcedimento mcp = new ModelProcedimento();
            String uuid = java.util.UUID.randomUUID().toString();
            mcp.setidProc(uuid);
            mcp.setProcedimento(edtNomeProc.getText());
            mcp.setQtdMaxPAciente(Integer.parseInt(comboQtdPacientes.getSelectedItem().toString()));

            FireBaseClient fbc = new FireBaseClient();
            DocumentReference docRef = fbc.FirebaseInt().collection("procedimentos").document(uuid);
            ApiFuture<WriteResult> result = docRef.set(mcp);
            try {
                System.out.println("Update time : " + result.get().getUpdateTime());
                Component frame = null;
                JOptionPane.showMessageDialog(frame,
                        "Procedimento cadastrado com sucesso!");
                edtNomeProc.setText("");

            } catch (InterruptedException | ExecutionException ex) {
                Logger.getLogger(Anamnese.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btnInserirProcActionPerformed

