    private void btnConfirmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmaActionPerformed

        //buscar ID do profissional
        ModelCadastroFunc func = mdfunc.stream()
                .filter(nome -> comboFunc.getSelectedItem().toString().equals(nome.getNomeFunc()))
                .findAny()
                .orElse(null);

        FireBaseClient fbclient = new FireBaseClient();
        UserRecord userRecord = null;
        try {
            userRecord = fbclient.FirebaseAuth().getUserByEmail(func.getLogin());
            UserRecord.UpdateRequest request = new UserRecord.UpdateRequest(userRecord.getUid())
                    .setDisabled(true);
            userRecord = FirebaseAuth.getInstance().updateUser(request);
        } catch (FirebaseAuthException ex) {
            Logger.getLogger(DesabilitaUser.class.getName()).log(Level.SEVERE, null, ex);
        }

        System.out.println("Successfully updated user: " + userRecord.getUid());
        Component frame = null;
        JOptionPane.showMessageDialog(frame,
                "Usuário desabilitado com sucesso!");
    }//GEN-LAST:event_btnConfirmaActionPerformed

